# Add function
def add(first,second):
    answer = (first + second)
    return answer

# Subtract function
def subtract(first,second):
    answer = (first - second)
    return answer

# Divide function
def divide(first,second):
    answer = (first / second)
    return answer

# Body - print results using each function above
print(add(1,2))
print(subtract(4,1))
print(divide(8,2))